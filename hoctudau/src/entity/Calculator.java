package entity;

public class Calculator {
   public static int add(int a, int b) {
        int c = a + b;
        return c;

    }
   public static int minus(int a, int b) {
        int c = a - b;
        return c;

    }
    public static int multiplication(int a, int b) {
       int c = a * b;
       return c;
    }
    public static float division(float a, float b) {
       float c = a / b;
       return c;
    }
}
